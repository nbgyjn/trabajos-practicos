/** \brief la funcion pide un al usuario ingresar un numero.
 *
 * \param mensaje[] char  Mensaje a ser mostrado.
 * \return float devuelve un numero
 *
 */
float pedirnumero(char mensaje[]);

/** \brief la funcion hace la suma de dos numeros
 *
 * \param numero1 float valor del primer numero
 * \param numero2 float valor del segundo numero
 * \return float  devuelve el resultado de la operacion
 *
 */
float sumar(float numero1, float numero2);

/** \brief la funcion hace la resta de dos numeros
 *
 * \param numero1 float valor del primer numero
 * \param numero2 float valor del segundo numero
 * \return float        devuelve el resultado de la operacion
 *
 */
float restar(float numero1, float numero2);

/** \brief la funcion hace la divicion del primer valor por el segundo.
 *
 * \param numero1 float valor del dividendo
 * \param numero2 float valor del divisor
 * \return float        devuelve el resultado de la operacion
 *
 */
float dividir(float numero1, float numero2);

/** \brief la funcion hace la multiplicacion de dos numeros
 *
 * \param numero1 float valor del primer numero
 * \param numero2 float valor del segundo numero
 * \return float        devuelve el resultado de la operacion
 *
 */
float multiplicar(float numero1, float numero2);

/** \brief la funcion da  el factorial de un numero
 *
 * \param numero1 float valor del numero a operar
 * \return void         vacio, imprime los posibles resultados
 *
 */
void factorial( float numero1);
